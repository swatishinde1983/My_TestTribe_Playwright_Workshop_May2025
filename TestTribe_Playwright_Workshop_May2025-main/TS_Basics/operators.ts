// Arithmetic Operators
let a = 10;
let b = 5;
let sum = a + b; // Addition
let difference = a - b; // Subtraction
let product = a * b; // Multiplication
let quotient = a / b; // Division and get quotient
let remainder = a % b; // Modulus (Remainder)

// Comparison Operators
let x = 20;
let y = 10;
let isEqual = x === y; // Strict equality
let isEqualLoose = x == y; // Loose equality
let isNotEqual = x !== y; // Strict inequality
let isNotEqualLoose = x != y; // Loose inequality
let isGreater = x > y; // Greater than
let isGreaterOrEqual = x >= y; // Greater than or equal to
let isLess = x < y; // Less than
let isLessOrEqual = x <= y; // Less than or equal to

// Logical Operators
let condition1 = true;
let condition2 = false;
let andResult = condition1 && condition2; // Logical AND
let orResult = condition1 || condition2; // Logical OR
let notResult = !condition1; // Logical NOT

// Assignment Operators
let num = 10;
num += 5; // Add and assign (num = num + 5)
num -= 3; // Subtract and assign (num = num - 3)
num *= 2; // Multiply and assign (num = num * 2)
num /= 4; // Divide and assign (num = num / 4)
num %= 3; // Modulus and assign (num = num % 3)

// Ternary Operator
let age = 10;
let canVote = (age >=18) ? "Yes" : "No"; // Ternary operator for conditional assignment
console.log(canVote);