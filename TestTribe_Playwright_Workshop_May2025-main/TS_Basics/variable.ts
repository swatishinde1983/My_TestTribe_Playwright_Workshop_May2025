// Syntax: variable declaration
// var variableName = value;
// let variableName = value;
// const variableName = value;

// var
var myName = "Mahesh";
console.log(myName);

// let
let myAge = 30;
console.log(myAge);

// const
const myGender = "male";
console.log(myGender);

// Reassigning variables
//syntax: variableName = newValue;
myName = "John";
console.log(myName);
myAge = 31;
console.log(myAge);

// myGender = "female"; // This will throw an error because const variables cannot be reassigned